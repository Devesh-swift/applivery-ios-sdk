import Foundation


extension UIColor {
	
	class func AppliveryBig() -> UIColor {
		return UIColor.FromHex(0x6494A5)
	}
	
	class func LabelBlack() -> UIColor {
		return UIColor.FromHex(0x3C4353)
	}
	
}